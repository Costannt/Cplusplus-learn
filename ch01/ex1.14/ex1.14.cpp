/*
一、for 循环
优点：
结构清晰：
for 循环将初始化、条件判断和更新操作集中写在一行中，逻辑紧凑，便于理解循环的整体意图。
适用于已知迭代次数的场景：
当你知道循环要执行多少次（如遍历数组、容器、固定次数的操作等），for 循环是最自然的选择。
作用域控制好（C++98 及以后）：
在 for (int i = 0; ...) 中声明的变量 i 仅在循环体内可见，避免污染外部作用域。
支持范围 for（C++11 起）：
对容器或数组进行遍历时，范围 for（range-based for loop）语法简洁安全：
cpp

编辑



for (const auto& item : container) { ... }
缺点：
灵活性较低：
如果循环的控制逻辑复杂（比如多个条件、非线性步长、中途修改循环变量等），for 循环可能显得笨拙甚至难以表达。
容易误用：
比如在循环体内部修改循环变量，可能导致逻辑错误或无限循环。
二、while 循环
优点：
灵活性高：
适用于循环次数未知、依赖运行时条件的场景（如读取输入直到 EOF、等待某个事件发生等）。
逻辑更贴近自然语言：
“当……条件成立时，就一直做某事”，语义直观。
适合复杂控制流：
当初始化、更新、判断分布在不同位置时，while 更自然。
缺点：
容易遗漏更新语句：
因为更新操作不在循环头部，若忘记在循环体内更新控制变量，容易造成死循环。
变量作用域更广：
控制变量通常在 while 之前声明，生命周期延续到循环之后，可能带来不必要的副作用或命名冲突。
可读性可能较差：
如果初始化、判断、更新分散在代码各处，阅读者需要跳转多处才能理解整个循环逻辑。
*/